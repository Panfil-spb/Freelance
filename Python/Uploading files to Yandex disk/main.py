# подключаем библиотеку которую будем использовать
import yadisk

# объявляем функцию для подключения к яндекс диску
def login():
    # вводим токен доступа который получили по адрессу описанным в инструкции
    # input - функция которая получает переменную
    # текст в двойных ковычках будет выводиться перед вводом
    Token = input("Введите токен доступа: ")
    # получаем объект приложения, яндекс диск который мы будем использовать
    Disk = yadisk.YaDisk(token=Token)
    # возвращаем наш объект
    return Disk

# функция для загрузки файла
# функция принимает наш объект приложения
def upload_file(Disk):
    # получаем адрес файла который надо загрузить
    file_pc = input("Введите адресс файла, который надо загрузить: ")
    # введите название файла которое у него будет на диске
    file_disk = input("Введите название файла, которое у него будет на диске: ")
    Disk.upload(file_pc, "/" + file_disk)

