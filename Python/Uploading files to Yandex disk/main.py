# главная функция программы
def main():
    # получаем доступ к диску
    Disk = login()
    while True:
        print("\n\n\n1. Загрузка файла")
        print("2. Вывод файла")
        print("0. Выход")
        c = input("Ваш выбор")
        if c == '1':
            # загружаем файл
            upload_file(Disk)
